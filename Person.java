public class Person {  // class name person
    //Syntax datatype method/attribute = value;
    int age = 20 ;
    public static void main (String[] args)
    {
        // syntax ClassName Object = new Classname ;
        Person Jashu = new Person() ; 
        //Creating an object og class 
        // ObjectName.Attribute/method
        System.out.println(Jashu.age); 
    }
}
